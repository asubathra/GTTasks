package org.empsample;

public class EmployeeCode {
private void javaclass() {
	System.out.println("java class is started");
}
private void python() {
	System.out.println("Python class is not started");
}
public static void main(String[] args) {
	EmployeeCode e = new EmployeeCode();
	
	e.javaclass();
	e.python();
}
}
